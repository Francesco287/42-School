#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	d;

	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-nb);
	}
	else
	{
		d = nb + 48;
		write(1, &d, 1);
	}
}
